package com.superdata.pm.entity;

public class DownloadInfo {

	private int id;
	private String path;
	private int threadId;
	private int downloadLength;
	
	
	public DownloadInfo(){
		
	}

	public DownloadInfo(String path, int threadId, int downloadLength) {
		super();
		this.path = path;
		this.threadId = threadId;
		this.downloadLength = downloadLength;
	}

	public DownloadInfo(int id, String path, int threadId, int downloadLength) {
		super();
		this.id = id;
		this.path = path;
		this.threadId = threadId;
		this.downloadLength = downloadLength;
	}

	@Override
	public String toString() {
		// TODO Auto-generated method stub
		return "DownloadInfo [id=" + id + ", path=" + path + ", threadId="
		+ threadId + ", downloadLength=" + downloadLength + "]";
	}

	public int getId() {
		return id;
	}

	public void setId(int id) {
		this.id = id;
	}

	public String getPath() {
		return path;
	}

	public void setPath(String path) {
		this.path = path;
	}

	public int getThreadId() {
		return threadId;
	}

	public void setThreadId(int threadId) {
		this.threadId = threadId;
	}

	public int getDownloadLength() {
		return downloadLength;
	}

	public void setDownloadLength(int downloadLength) {
		this.downloadLength = downloadLength;
	}
	
	
	
}
