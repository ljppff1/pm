package com.superdata.pm.entity;

import android.widget.TextView;

/**
 * ExpandableListView 子列表实体
 * @author kw
 *
 */
public class ExpandableChildren {

	private TextView task_name; //任务名称
	private TextView task_startdata; //任务开始时间
	private TextView task_enddata; //任务结束时间
	private TextView task_duration; //任务持续时间
	private TextView task_person; //任务负责人
	
	public ExpandableChildren(TextView task_name, TextView task_startdata,
			TextView task_enddata, TextView task_duration, TextView task_person) {
		super();
		this.task_name = task_name;
		this.task_startdata = task_startdata;
		this.task_enddata = task_enddata;
		this.task_duration = task_duration;
		this.task_person = task_person;
	}

	public ExpandableChildren() {
		super();
	}

	public TextView getTask_name() {
		return task_name;
	}

	public void setTask_name(TextView task_name) {
		this.task_name = task_name;
	}

	public TextView getTask_startdata() {
		return task_startdata;
	}

	public void setTask_startdata(TextView task_startdata) {
		this.task_startdata = task_startdata;
	}

	public TextView getTask_enddata() {
		return task_enddata;
	}

	public void setTask_enddata(TextView task_enddata) {
		this.task_enddata = task_enddata;
	}

	public TextView getTask_duration() {
		return task_duration;
	}

	public void setTask_duration(TextView task_duration) {
		this.task_duration = task_duration;
	}

	public TextView getTask_person() {
		return task_person;
	}

	public void setTask_person(TextView task_person) {
		this.task_person = task_person;
	}
	
	
}
