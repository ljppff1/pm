package com.superdata.pm.entity;

public class entitycostexpenses {

	// 费用单号 id
	private String id;
	// 费用单号 code
	private String code;
	// 费用金额
	private String amount;
	// 费用描述
	private String remark;
	// 是否审核
	private String isverify;
	// 审核人
	private String auditname;
	// 日期
	private String date;
	// 所属项目
	private String projectby;
	// 项目id
	private String projectId;
	// 项目name
	private String projectName;

	public String getId() {
		return id;
	}

	public void setId(String id) {
		this.id = id;
	}

	public String getCode() {
		return code;
	}

	public void setCode(String code) {
		this.code = code;
	}

	public String getAmount() {
		return amount;
	}

	public void setAmount(String amount) {
		this.amount = amount;
	}

	public String getRemark() {
		return remark;
	}

	public void setRemark(String remark) {
		this.remark = remark;
	}

	public String getIsverify() {
		return isverify;
	}

	public void setIsverify(String isverify) {
		this.isverify = isverify;
	}

	public String getAuditname() {
		return auditname;
	}

	public void setAuditname(String auditname) {
		this.auditname = auditname;
	}

	public String getDate() {
		return date;
	}

	public void setDate(String date) {
		this.date = date;
	}

	public String getProjectby() {
		return projectby;
	}

	public void setProjectby(String projectby) {
		this.projectby = projectby;
	}

	public String getProjectId() {
		return projectId;
	}

	public void setProjectId(String projectId) {
		this.projectId = projectId;
	}

	public String getProjectName() {
		return projectName;
	}

	public void setProjectName(String projectName) {
		this.projectName = projectName;
	}

	/**
	 * 费用单
	 * 
	 * @param id
	 * @param code
	 * @param amount
	 * @param remark
	 * @param isverify
	 * @param auditname
	 * @param date
	 * @param projectName
	 */
	public entitycostexpenses(String id, String code, String amount,
			String remark, String isverify, String auditname, String date,
			String projectName) {
		super();
		this.id = id;
		this.code = code;
		this.amount = amount;
		this.remark = remark;
		this.isverify = isverify;
		this.auditname = auditname;
		this.date = date;
		this.projectName = projectName;
	}

	/**
	 * 材料的构造器
	 * 
	 * @param id
	 *            材料ID
	 * @param code
	 *            材料单号
	 * @param amount
	 *            总金额
	 * @param remark
	 *            领用描述
	 * @param isverify
	 *            是否审核
	 * @param auditname
	 *            审核人
	 * @param date
	 *            日期
	 * @param projectby
	 *            所属项目
	 */
	public entitycostexpenses(String id, String code, String amount,
			String remark, String isverify, String auditname, String date,
			String projectby, String projectId) {
		super();
		this.id = id;
		this.code = code;
		this.amount = amount;
		this.remark = remark;
		this.isverify = isverify;
		this.auditname = auditname;
		this.date = date;
		this.projectby = projectby;
		this.projectId = projectId;
	}

}
